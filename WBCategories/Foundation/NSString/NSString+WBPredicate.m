#pragma mark -- 字符串处理
#pragma mark
- (NSString *)wb_trim {
    NSCharacterSet *set = [NSCharacterSet whitespaceCharacterSet];
    return [self stringByTrimmingCharactersInSet:set];
}
- (NSString *)wb_removeWhiteSpacesFromString
{
    NSString *trimmedString = [self stringByTrimmingCharactersInSet:
                               [NSCharacterSet whitespaceAndNewlineCharacterSet]];
    return trimmedString;
}

#pragma mark -- 字符串判断
#pragma mark
- (BOOL)wb_isBlank {
    return ([[self wb_removeWhiteSpacesFromString] isEqualToString:@""]) ? YES : NO;
}

- (BOOL)wb_isNull {
    return ([[self wb_removeWhiteSpacesFromString] isEqualToString:@""] || self == nil || [self isEqualToString:@"(null)"]) ? YES : NO;
}

+ (BOOL)wb_isNull:(NSString *)object
{
    // 判断是否为空串
    if([object isKindOfClass:[NSNull class]])
        return YES;
    if ([object isEqual:[NSNull null]]) {
        return YES;
    } else {
        object = object.wb_trim;
        if (object != nil && object.length > 0 && ![object isEqualToString:@"null"]&&![object isEqualToString:@"(null)"]&&![object isEqualToString:@" "]) {
            return  NO;
        }
        return YES;
    }
}

- (BOOL)wb_containsString:(NSString *)string {
    if (string == nil) return NO;
    return [self rangeOfString:string].location != NSNotFound;
}

